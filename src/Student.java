    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
          
         try {
             
             d = (DefaultTableModel)jTable1.getModel();
             int selectIndex = jTable1.getSelectedRow();
         
         
            String ID = d.getValueAt(selectIndex, 0).toString();
            
            
            String AcadamicYear = jTextField1.getText();
            String AcademicSemester=jTextField4.getText();
            String Programe =jComboBox1.getSelectedItem().toString();
            //Integer groupnumber = (Integer)jSpinner1.getValue();
            //String GroupNumber = groupnumber.toString();
            //Integer subgroupnumber = (Integer)jSpinner2.getValue();
            //String SubGroupNumber = subgroupnumber.toString();
            String GroupNumber =jComboBox2.getSelectedItem().toString();
            String SubGroupNumber =jComboBox3.getSelectedItem().toString();
            String GroupID = jTextField2.getText();
            String SubGroupID = jTextField3.getText();
         
            pst = con.prepareStatement("update student set AcademicYear =?, AcademicSmester=?, Programe=?, GroupNumber=?, SubGroupNumber=?,GroupID=?,SubGroupID=? where ID = ?");
            
            pst.setString(1, AcadamicYear);
            pst.setString(2, AcademicSemester);
            pst.setString(3, Programe);
            pst.setString(4, GroupNumber);
            pst.setString(5, SubGroupNumber);
            pst.setString(6, GroupID);
            pst.setString(7, SubGroupID);
            pst.setString(8, ID);  
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Student Updated");   
            
             jButton2.setEnabled(true);
           
            
            jTextField1.setText("");
            jTextField4.setText("");
            jComboBox1.setSelectedIndex(-1);
           // jSpinner1.setValue("");
            //jSpinner2.setValue("");
            jComboBox2.setSelectedIndex(-1);
            jComboBox3.setSelectedIndex(-1);
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField1.requestFocus();
            
            load();
              
        } catch (SQLException ex) {
            Logger.getLogger(Student.class.getName()).log(Level.SEVERE, null, ex);
        }
 
    }//GEN-LAST:event_jButton3ActionPerformed

