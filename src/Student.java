          public void load(){
    
         int c;
            try {
            pst = con.prepareStatement("select * from student");
            rs = pst.executeQuery();
            
            ResultSetMetaData rsd = rs.getMetaData();
            c = rsd.getColumnCount();
            
            d = (DefaultTableModel)jTable1.getModel();
            d.setRowCount(0);
            
            
            
             while(rs.next()){
             
                 Vector v2 = new Vector();
                 
                 for(int i=1; i<=c; i++){
                 
                    v2.add(rs.getString("ID"));
                    v2.add(rs.getString("AcademicYear"));
                    v2.add(rs.getString("AcademicSmester"));
                    v2.add(rs.getString("Programe"));
                    v2.add(rs.getString("GroupNumber"));
                    v2.add(rs.getString("SubGroupNumber"));
                    v2.add(rs.getString("GroupID"));
                    v2.add(rs.getString("SubGroupID"));
                 }
             
                    d.addRow(v2);
             }
            
        } catch (SQLException ex) {
            Logger.getLogger(Student.class.getName()).log(Level.SEVERE, null, ex);
        }
          }
